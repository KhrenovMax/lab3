package com.example.polina.myapplication;

public class Product {

    String title;
    String country;
    String rating;
    String description;
    int poster;

    public Product(String title, String country, String rating, String description, int poster) {
        this.title = title;
        this.country = country;
        this.rating = rating;
        this.description = description;
        this.poster = poster;
    }
}